    public void BuscarEmpleado() {

        String codigo_empleado = jTextField1.getText();

        int codiguito;
        codiguito = Integer.parseInt(codigo_empleado);

        try {

            String cadcon = "jdbc:mysql://localhost/conce?serverTimezone=UTC";
            String user = "root";
            String password = "";

            Connection conexion = DriverManager.getConnection(cadcon, user, password);
            
            Statement sentencia = conexion.createStatement();

            ResultSet resultado = sentencia.executeQuery("Select * from empleadosintento WHERE IdEmpleado = "+ codiguito);

            FileWriter fw = new FileWriter("BuscarEmpleado.txt", true);

            while (resultado.next()) {

                //Mapeo
                int cosigo = resultado.getInt(1);
                String nom = resultado.getString(2);
                String ape = resultado.getString(3);
                int ed = resultado.getInt(4);
                String dir = resultado.getString(5);
                int dept = resultado.getInt(6);

                fw.write("Empleado Buscado:\n\nCodigo empleado: " + cosigo + "\nNombre: " + nom + "\nApellidos: " + ape + "\nEdad: " + ed + "\nDirección: " + dir + "\nDepartamento: " + dept + "\n");

                o[0] = cosigo;
                o[1] = nom;
                o[2] = ape;
                o[3] = ed;
                o[4] = dir;
                o[5] = dept;
                dtm.addRow(o);
            }
            
            fw.close();
            resultado.close();
            sentencia.close();
            conexion.close();

        }catch(IOException io){
            io.getMessage();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

